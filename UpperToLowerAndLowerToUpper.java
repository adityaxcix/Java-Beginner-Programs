package stringPrograms1_10;
public class UpperToLowerAndLowerToUpper
{
	public static void main(String[] args) 
	{
		//Converting UpperCase to LowerCase ? Don't worry, Just add 32 and Vice versa
		
		//UpperCase
			char ch = 'A';
			ch+=32;
			System.out.println(ch);
		//LowerCase
			char ch1 = 'a';
			ch1-=32;
			System.out.println(ch1);
			
			
			
			
	}
}